# Basic string for-loop
for letter in 'hello!':
    print('Current letter: ', letter)

# Basic int for-loop
for counter in range(1, 11):
    print(counter)
print('Finished')

# Basic int for-loop, starts with 0 by default
for counter in range(11):
    print(counter)
print('Finished')

# Basic int for-loop, increments by 2
for counter in range(1, 11, 2):
    print(counter)
print('Finished')